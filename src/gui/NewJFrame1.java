    private void progressAnimation() {
        new Thread(new Runnable() {

            @Override
            public void run() {

                for (int i = 0; i < 100; i++) {
                    jProgressBar1.setValue(i);
                    
                    if (i == 10){
                        jLabel1.setText("Database is loading.....");
                    }
                    
                    try {
                        Thread.sleep(500);
                    } catch (InterruptedException e) {
                        e.printStackTrace();
                    }
                }
                
                new NewJFrame().setVisible(true);
                obj.dispose();

            }

        }).start();
    }

